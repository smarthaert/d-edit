//   Ввести целочисленную матрицу A размером 6x6 элементов.
//   Не используя дополнительных массивов, переставить столбцы
//   матрицы A таким образом, чтобы при увеличении номера столбца
//   уменьшалось или оставалось неизменным минимальное значение
//   среди элементов столбца, а затем для каждого столбца поменять
//   местами значения главной диагонали и элемента с минимальным
//   значением в данном столбце.

#include <stdio.h>
#include <conio.h>
#include <values.h>

// ---=== Матрица 6x6 ===---
int A[6][6];

// ---=== Номер строки элемента с min значением в данном столбце ===---
int min_el( int num ) // num - номер столбца
 {
   int cur_min = MAXINT; // Текущий миниум
   int num_min; // Номер минимального элемента
   for(int i=0;i<6;i++) // Пробегаем все элементы данного столбца
	// Если какой-либо элемент меньше cur_min => cur_min = этот_элемент
	 if (A[i][num] < cur_min) { cur_min = A[i][num]; num_min = i; };
   return num_min; // Возвращаем результат поиска
 };

// ---=== Основная программа ===---
void main()
 {
   int i,j,k,temp; // Три счетчика и одна временная переменная
  // Вводим матрицу с клавиатуры
   clrscr();
   printf("Введите элементы матрицы A(6x6) (Вводите только целые числа): ");
   for(i=0;i<6;i++)
	 for(j=0;j<6;j++)
	  {
		gotoxy(j*10+1,i+2);
		scanf("%d",&A[i][j]);
	  };
  // Сортируем столбцы по убыванию минималього элемента
   for(i=0;i<5;i++)
	 for(j=i;j<6;j++)
	  {
		if (A[min_el(i),i] < A[min_el(j),j])
		  for(k=0;k<6;k++)
			{ temp = A[k][i]; A[k][i] = A[k][j]; A[k][j] = temp; };
	  };
  // Меняем местами минимальный элемент и элемент главной диагонали
   for(i=0;i<6;i++)
	{ j = min_el(i); temp = A[i][i]; A[i][i] = A[j][i]; A[j][i] = temp; }
  // Вывод матрицы на экран (для просмотра результатов работы)
   gotoxy(1,8);
   printf("Преобразованная матрица:");
   for(i=0;i<6;i++)
	 for(j=0;j<6;j++)
	  {
		gotoxy(j*10+1,i+9);
		printf("%d",A[i][j]);
	  };
  // Выход из программы
   gotoxy(1,17);
   printf("---=== Нажмите на любую клавишу для выхода ===---");
   getch();
 }
